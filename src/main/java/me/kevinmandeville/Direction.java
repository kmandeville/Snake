package me.kevinmandeville;

/**
 * Represents the possible directional movements.
 * <p>
 * This enumeration is primarily used for defining the movement direction in a 2D space. The available directions
 * include:
 * - UP: Represents upward movement.
 * - DOWN: Represents downward movement.
 * - LEFT: Represents movement to the left.
 * - RIGHT: Represents movement to the right.
 * <p>
 * Typically used in applications where directional control is required, such as navigating objects on a grid or
 * handling key input for movement logic.
 */
public enum Direction {
    UP, DOWN, LEFT, RIGHT
}
